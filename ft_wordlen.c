#include "libft.h"

int		ft_wordlen(char *s, char c)
{
	int		i;
	int		len;

	len = 0;
	i = 0;
	while (s[i] && s[i] == c)
		i++;
	while (s[i] && s[i] != c)
	{
		i++;
		len++;
	}
	return (len);
}
